days={"monday","tuesday","wednesday","thursday","friday","saturday","sunday","sunday","sunday","sunday"}
print(days)
# set methods
days.add("mon")
print(days)
# remove=removes values in a set
days.remove("mon")
print(days)
# update adds more values

days.update([1,2,3])
print(days)
# clear
days.clear
print(days)


# discard
days.discard
print(days)